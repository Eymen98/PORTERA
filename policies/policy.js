function applyPolicyPage() {
  const pageType = document.body.dataset.policy;
  const savedLang = localStorage.getItem("lang") || "tr";
  const lang = ["tr", "en", "ar"].includes(savedLang) ? savedLang : "tr";

  const pageData = policyTranslations[pageType]?.[lang];
  const uiData = policyUI[lang];

  if (!pageData || !uiData) return;

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title = pageData.pageTitle;

  const brandEl = document.getElementById("policyBrand");
  const titleEl = document.getElementById("policyTitle");
  const contentEl = document.getElementById("policyContent");
  const updatedEl = document.getElementById("policyUpdated");

  const navAboutEl = document.getElementById("navAbout");
  const navContactEl = document.getElementById("navContact");
  const navFaqEl = document.getElementById("navFaq");
  const mobileNavAboutEl = document.getElementById("mobileNavAbout");
  const mobileNavContactEl = document.getElementById("mobileNavContact");
  const mobileNavFaqEl = document.getElementById("mobileNavFaq");

  if (brandEl) brandEl.textContent = pageData.brand;
  if (titleEl) titleEl.textContent = pageData.title;
  if (updatedEl) updatedEl.textContent = pageData.updated;

  if (navAboutEl) navAboutEl.textContent = uiData.navAbout;
  if (navContactEl) navContactEl.textContent = uiData.navContact;
  if (navFaqEl) navFaqEl.textContent = uiData.navFaq;

  if (mobileNavAboutEl) mobileNavAboutEl.textContent = uiData.navAbout;
  if (mobileNavContactEl) mobileNavContactEl.textContent = uiData.navContact;
  if (mobileNavFaqEl) mobileNavFaqEl.textContent = uiData.navFaq;

  if (contentEl) {
    contentEl.innerHTML = pageData.content.map(text => `<p>${text}</p>`).join("");
  }
}

document.addEventListener("DOMContentLoaded", applyPolicyPage);